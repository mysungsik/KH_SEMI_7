-- 4. 뉴스 출력
SELECT N.*, U.USER_NAME, T.TEAM_NAME 
FROM NEWS N
JOIN USER_INFO U ON N.USER_NO = U.USER_NO
JOIN TEAMS T ON N.TEAM_NO = T.TEAM_NO
WHERE N.NEWS_NO = 1;


-- 5. 팀 정보(이름, 색깔, 로고) 출력
SELECT TEAM_NAME, 
(SELECT IMG_ORIGINAL FROM TEAM_IMG WHERE IMG_LEVEL=1) IMG_ORIGINAL, 
TEAM_COLOR  FROM TEAMS
JOIN TEAM_IMG USING(TEAM_NO)
WHERE TEAM_NO = 1
AND TEAM_ST = 'Y';

-- 5. 각 뉴스별 좋아요 출력
SELECT L.*, U.USER_NAME, T.LIKE_TYPE FROM "LIKE" L
JOIN USER_INFO U ON L.USER_NO = U.USER_NO
JOIN LIKE_TYPE T ON L.LIKE_TYPE_NO = T.LIKE_TYPE_NO
WHERE L.LIKE_TYPE_NO = 5 
	AND L.LIKE_TARGET_NO = 1;

-- 6. 각 뉴스별 댓글 출력
SELECT R.*, U.USER_NAME, T.REPLY_TYPE FROM REPLY R
JOIN USER_INFO U ON R.USER_NO = U.USER_NO
JOIN REPLY_TYPE T ON R.REPLY_TYPE_NO = T.REPLY_TYPE_NO
WHERE R.REPLY_TYPE_NO = 4 
	AND R.REPLY_TARGET_NO = 3
	AND R.REPLY_ST = 'N'
ORDER BY R.REPLY_DT ASC;
